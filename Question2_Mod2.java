// Program that performs arithmetic operations with values of type char.


public class Question2_Mod2 {
    public static void main(String args[]) {
        char ch1 = 'A';
        char ch2 = 'B';
        int sum = ch1+ch2;
        int prod = ch1*ch2;
        int difference = ch1-ch2;
        int division = ch1/ch2;
        int modOperaion = ch1%ch2;
        System.out.println("Addition Is : "+sum);
        System.out.println("Product Is : "+prod);
        System.out.println("Difference Is : "+difference);
        System.out.println("Division Is : "+division);
        System.out.println("Mod Operation Is : "+modOperaion);
    }
}
